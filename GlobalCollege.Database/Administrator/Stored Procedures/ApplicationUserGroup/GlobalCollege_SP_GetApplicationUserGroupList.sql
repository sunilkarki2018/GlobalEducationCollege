CREATE PROCEDURE [Administrator].[GlobalCollege_SP_GetApplicationUserGroupList]
     @ApplicationUserId UNIQUEIDENTIFIER = NULL
    ,@ApplicationGroupId UNIQUEIDENTIFIER = NULL
    ,@Remarks NVARCHAR(200) = NULL
    ,@RecordStatus INT = NULL
    ,@PageNumber INT
    ,@PageSize INT
    ,@Debug INT = 0
AS

BEGIN TRY

 SET NOCOUNT ON;

 DECLARE @ColumnList NVARCHAR(MAX),
         @TSQL NVARCHAR(MAX),
         @ParameterList NVARCHAR(MAX),
         @Where NVARCHAR(MAX) = '',
         @TotalRows DECIMAL(18, 2),
         @NextLine CHAR(2) = CHAR(13) + CHAR(10),
         @TCount INT


         SET @PageNumber = ISNULL(@PageNumber, 1)
         SET @PageSize = ISNULL(@PageSize, 20)   
         SET @TotalRows = 20 * 1.0; 


       SET @Where = +'WHERE 1=1 ' + CASE WHEN @ApplicationUserId IS NOT NULL THEN 'AND ApplicationUserGroup.ApplicationUserId = @ApplicationUserId ' ELSE '' END
     + CASE WHEN @ApplicationGroupId IS NOT NULL THEN 'AND ApplicationUserGroup.ApplicationGroupId = @ApplicationGroupId ' ELSE '' END
     + CASE WHEN @Remarks IS NOT NULL THEN 'AND ApplicationUserGroup.Remarks  LIKE  @Remarks ' ELSE '' END
     + CASE WHEN @RecordStatus IS NOT NULL THEN 'AND ApplicationUserGroup.RecordStatus = @RecordStatus ' ELSE ' AND ApplicationUserGroup.RecordStatus <> 1' END

    SELECT @TSQL = N';WITH ApplicationUserGroup_CTE 
                     AS
                     (
                        SELECT  [Id]
                               ,[ApplicationUserId]
                               ,[ApplicationGroupId]
                               ,[Remarks]
                               ,[TotalModification]
                               ,[CreatedBy]
                               ,[ModifiedBy]
                               ,[AuthorisedBy]
                               ,[CreatedById]
                               ,[ModifiedById]
                               ,[AuthorisedById]
                               ,[CreatedDate]
                               ,[ModifiedDate]
                               ,[AuthorisedDate]
                               ,[EntityState]
                               ,[RecordStatus]

                         FROM[Administrator].[ApplicationUserGroup](NOLOCK) AS ApplicationUserGroup '+@Where+')    , 
						 Count_CTE

                        AS
                        (
                            SELECT COUNT(*) AS TotalRows FROM ApplicationUserGroup_CTE
                        )


                        SELECT CONVERT(INT, (TotalRows / @TotalRows)) [PageCount],                                                                                          
	                    				   CONVERT(INT, TotalRows)[TotalRecords],
	                    				   ApplicationUserGroup.*
                        FROM ApplicationUserGroup_CTE AS ApplicationUserGroup
                        CROSS JOIN Count_CTE
                        
                        ORDER BY ModifiedDate DESC
                        
                        
                        OFFSET @PageSize * (@PageNumber - 1) ROWS
                        FETCH NEXT @PageSize ROWS ONLY 
	                     
	                     ';  


            IF @Debug = 1

            BEGIN
              PRINT @TSQL

            END
                   SET @ParameterList = N'@ApplicationUserId UNIQUEIDENTIFIER = NULL
                   ,@ApplicationGroupId UNIQUEIDENTIFIER = NULL
                   ,@Remarks NVARCHAR(200) = NULL

				   ,@RecordStatus INT
                   ,@PageNumber INT
                   ,@PageSize INT
                   ,@TotalRows INT
                    '
                 EXECUTE sp_executesql @TSQL,
                   @ParameterList,
                   @ApplicationUserId,
                   @ApplicationGroupId,
                   @Remarks,
       
                   @RecordStatus,
                   @PageNumber,
                   @PageSize,
                   @TotalRows

            END TRY




        BEGIN CATCH
        
        
        
        
        
          INSERT INTO[dbo].[ExceptionLoggers]
                ([Id]
                 ,[ExceptionMessage]
                 ,[ControllerName]
                 ,[ExceptionStackTrace]
                 ,[TotalModification]
                 ,[CreatedBy]
                 ,[ModifiedBy]
                 ,[AuthorisedBy]
                 ,[CreatedById]
                 ,[ModifiedById]
                 ,[AuthorisedById]
                 ,[CreatedDate]
                 ,[ModifiedDate]
                 ,[AuthorisedDate]
                 ,[EntityState]
                 ,[RecordStatus]
                 ,[DataEntry]
                 ,[ChangeLog]
                 )
             VALUES
                   (NEWID(),
                    ERROR_MESSAGE(),
                    'ApplicationUserGroup',
                    CONVERT(NVARCHAR(MAX), ERROR_LINE()) + ', ' + CONVERT(NVARCHAR(MAX), ERROR_NUMBER()) + ', ' + CONVERT(NVARCHAR(MAX), ERROR_STATE()),
                    1,
                    'administrator',
                    'administrator',
                    'administrator',
                    'DE69AA3E-CC18-430F-9818-6B7A45691ECF',
                    'DE69AA3E-CC18-430F-9818-6B7A45691ECF',
                    'DE69AA3E-CC18-430F-9818-6B7A45691ECF',
                     GETDATE(),
                     GETDATE(),
                     GETDATE(),
                     1,
                     2,
                     2,
                     NULL
                    )
        
        
        
        
        
        END CATCH   